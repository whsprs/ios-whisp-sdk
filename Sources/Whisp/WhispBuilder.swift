/// `Whisp` is a high-level chat framework built on top of `WhispAgent`, providing
/// a complete solution for chat-based interactions with *whisp*.
///
/// This framework handles all user-side chat logic including:
/// - Message management and persistence
/// - Chat session handling
/// - Network communication with *whisp*
/// - State management and chat flow control
///
/// While `WhispAgent` provides low-level communication primitives,
/// this framework manages the complete chat lifecycle and business logic,
/// making it easier to integrate *whisp* into wallet applications.
///
/// Note: This framework is currently in development. Public APIs may change.
